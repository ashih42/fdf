#include "libft.h"

int		ft_sqrt(int n)
{
	int	guess;

	if (n <= 0)
		return (0);
	guess = 1;
	while (guess * guess < n)
	{
		guess++;
		if (guess == 46340)
			break ;
	}
	return (guess);
}
